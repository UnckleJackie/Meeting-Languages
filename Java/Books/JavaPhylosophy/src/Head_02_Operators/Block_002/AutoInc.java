package Head_02_Operators.Block_002;

import static java.lang.System.out;

public class AutoInc {
    public static void main(String[] args) {
        int i = 1;
        out.println("i : " + i);
        out.println("++i : " + ++i);
        out.println("i++ : " + i++);
        out.println("i : " + i);
        out.println("--i : " + --i);
        out.println("i-- : " + i--);
        out.println("i : " + i);
    }
}
